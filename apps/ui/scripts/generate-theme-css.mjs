import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const genericFontFamilies = new Set([
  'serif',
  'sans-serif',
  'monospace',
  'cursive',
  'fantasy',
  'system-ui',
  'ui-serif',
  'ui-sans-serif',
  'ui-monospace',
  'ui-rounded',
]);

function tokenEntries(group) {
  return Object.entries(group).filter(([key]) => !key.startsWith('$'));
}

function getToken(tokens, path) {
  const value = path.split('.').reduce((current, segment) => {
    if (current && typeof current === 'object' && segment in current) {
      return current[segment];
    }

    throw new Error(`Unknown token reference: {${path}}`);
  }, tokens);

  if (!value || typeof value !== 'object' || !('$value' in value)) {
    throw new Error(
      `Token reference does not point to a token value: {${path}}`,
    );
  }

  return value;
}

function resolveTokenValue(tokens, value) {
  const reference = typeof value === 'string' && value.match(/^\{(.+)}$/);

  if (reference) {
    return resolveTokenValue(tokens, getToken(tokens, reference[1]).$value);
  }

  if (value && typeof value === 'object' && 'hex' in value) {
    return value.hex;
  }

  return value;
}

function formatFontFamily(value) {
  if (!Array.isArray(value)) {
    throw new Error('Font family token values must be arrays.');
  }

  return value
    .map((family) => {
      if (typeof family !== 'string') {
        throw new Error('Font family entries must be strings.');
      }

      if (genericFontFamilies.has(family)) {
        return family;
      }

      return `"${family}"`;
    })
    .join(', ');
}

function pushVariable(lines, name, value) {
  if (typeof value !== 'string' && typeof value !== 'number') {
    throw new Error(`CSS variable ${name} must resolve to a string or number.`);
  }

  lines.push(`  ${name}: ${value};`);
}

export function generateThemeCss(tokens) {
  const lines = [
    '/* This file is generated by `pnpm tokens:build`. Do not edit directly. */',
    '@theme static {',
  ];

  for (const [name, token] of tokenEntries(tokens.color)) {
    pushVariable(
      lines,
      `--color-${name}`,
      resolveTokenValue(tokens, token.$value),
    );
  }

  lines.push('');

  for (const [name, token] of tokenEntries(tokens.typography['font-family'])) {
    pushVariable(
      lines,
      `--font-${name}`,
      formatFontFamily(resolveTokenValue(tokens, token.$value)),
    );
  }

  lines.push('');

  for (const [name, token] of tokenEntries(tokens.typography)) {
    if (name === 'font-family') {
      continue;
    }

    const value = resolveTokenValue(tokens, token.$value);

    if (!value || typeof value !== 'object' || !('fontSize' in value)) {
      throw new Error(`Typography token ${name} must include fontSize.`);
    }

    pushVariable(lines, `--text-${name}`, value.fontSize);

    if ('lineHeight' in value) {
      pushVariable(
        lines,
        `--text-${name}--line-height`,
        value.lineHeight,
      );
    }

    if ('fontWeight' in value) {
      pushVariable(
        lines,
        `--text-${name}--font-weight`,
        value.fontWeight,
      );
    }
  }

  lines.push('');

  for (const [name, token] of tokenEntries(tokens.radius)) {
    pushVariable(
      lines,
      `--radius-${name}`,
      resolveTokenValue(tokens, token.$value),
    );
  }

  lines.push('');

  for (const [name, token] of tokenEntries(tokens.spacing)) {
    const cssName =
      name === 'container-max' ? '--container-content' : `--spacing-${name}`;
    pushVariable(lines, cssName, resolveTokenValue(tokens, token.$value));
  }

  lines.push('}', '');

  return lines.join('\n');
}

async function main() {
  const scriptDir = dirname(fileURLToPath(import.meta.url));
  const appRoot = resolve(scriptDir, '..');
  const tokensPath = resolve(appRoot, 'src/styles/design-tokens.json');
  const themePath = resolve(appRoot, 'src/styles/theme.css');
  const checkOnly = process.argv.includes('--check');
  const tokens = JSON.parse(await readFile(tokensPath, 'utf8'));
  const generatedTheme = generateThemeCss(tokens);

  if (checkOnly) {
    const currentTheme = await readFile(themePath, 'utf8');

    if (currentTheme !== generatedTheme) {
      console.error(
        'src/styles/theme.css is out of sync with src/styles/design-tokens.json. Run `pnpm tokens:build`.',
      );
      process.exit(1);
    }

    console.log('theme.css is in sync with design-tokens.json');
  } else {
    await writeFile(themePath, generatedTheme);
    console.log(
      'Generated src/styles/theme.css from src/styles/design-tokens.json',
    );
  }
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  await main();
}
